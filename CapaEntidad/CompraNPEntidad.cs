namespace CapaEntidad
{
    public class CompraNPEntidad
    {
       
        private string Empresa, Descripcion, Unidad, Cantidad, CantCN, VentaSoles, CompraSoles, Utilidad;

    public string tbEmpresa
        {
        get { return Empresa; }
        set { Empresa = value; }
    }

    public string tbDescripcion
        {
        get { return Descripcion; }
        set { Descripcion = value; }
    }

    public string tbUnidad
        {
        get { return Unidad; }
        set { Unidad = value; }
    }
    public string tbCantidad
        {
        get { return Cantidad; }
        set { Cantidad = value; }
    }
    public string tbCompraSoles
        {
        get { return CompraSoles; }
        set { CompraSoles = value; }
    }
    public string tbVentaSoles
        {
         get { return VentaSoles; }
         set { VentaSoles = value; }
    }
        public string tbUtilidad
        {
            get { return Utilidad; }
            set { Utilidad = value; }
        }

    }
}
