namespace CapaEntidad
{
    public class ClienteGrupoEntidad
    {

        private string
        CanalCod, CanalDesc, Vendedor, VenDes, RUC, RazonSocial, SubTotal;
    public string tbCanalCod
        {
        get { return CanalCod; }
        set { CanalCod = value; }
        }

    public string tbCanalDesc
        {
        get { return CanalDesc; }
        set { CanalDesc = value; }
        }

        public string tbVendedor
        {
            get { return Vendedor; }
            set { Vendedor = value; }
        }

        public string tbVenDes
        {
            get { return VenDes; }
            set { VenDes = value; }
        }
        public string tbRUC
        {
        get { return RUC; }
        set { RUC = value; }
        }
    public string tbRazonSocial
        {
        get { return RazonSocial; }
        set { RazonSocial = value; }
        }
    public string tbSubTotal
        {
            get { return SubTotal; }
            set { SubTotal = value; }
        }
       
    }
}
