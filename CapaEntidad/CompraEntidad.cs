namespace CapaEntidad
{
    public class CompraEntidad
    {
       
        private string Codigo, Descripcion, Unidad, Cantidad, CostoSoles, VentaSoles, Porcentaje;

    public string tbCodigo
        {
        get { return Codigo; }
        set { Codigo = value; }
    }

    public string tbDescripcion
        {
        get { return Descripcion; }
        set { Descripcion = value; }
    }

    public string tbUnidad
        {
        get { return Unidad; }
        set { Unidad = value; }
    }
    public string tbCantidad
        {
        get { return Cantidad; }
        set { Cantidad = value; }
    }
    public string tbCostoSoles
        {
        get { return CostoSoles; }
        set { CostoSoles = value; }
    }
    public string tbVentaSoles
        {
         get { return VentaSoles; }
         set { VentaSoles = value; }
    }
        public string tbPorcentaje
        {
            get { return Porcentaje; }
            set { Porcentaje = value; }
        }

    }
}
