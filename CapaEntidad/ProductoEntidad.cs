namespace CapaEntidad
{
    public class ProductoEntidad
    {

        private string Empresa, Codigo, CodigoAl, Descripcion, Unidad;

    public string tbEmpresa
        {
        get { return Empresa; }
        set { Empresa = value; }
    }

    public string tbCodigo
        {
        get { return Codigo; }
        set { Codigo = value; }
    }

    public string tbCodigoAl
        {
        get { return CodigoAl; }
        set { CodigoAl = value; }
    }
    public string tbDescripcion
        {
        get { return Descripcion; }
        set { Descripcion = value; }
    }
    public string tbUnidad
        {
        get { return Unidad; }
        set { Unidad = value; }
    }
}
}
