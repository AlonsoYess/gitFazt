namespace CapaEntidad
{
    public class VentasGCPVEntidad
    {
        private string
            CanalCod
            , CanalDesc
            , Vendedor
            , VenDes
            , SubTotal;
    
        public string tbCanalCod
        {
            get { return CanalCod; }
            set { CanalCod = value; }
        }
        public string tbCanalDesc
        {
            get { return CanalDesc; }
            set { CanalDesc = value; }
        }

        public string tbVendedor
        {
            get { return Vendedor; }
            set { Vendedor = value; }
        }

        public string tbVenDes
        {
            get { return VenDes; }
            set { VenDes = value; }
        }

        public string tbSubTotal
        {
            get { return SubTotal; }
            set { SubTotal = value; }
        }
       

    }
}
