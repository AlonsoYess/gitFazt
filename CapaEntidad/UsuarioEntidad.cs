namespace CapaEntidad
{
    public class UsuarioEntidad
    {
       
        private string usuario, clave, nombre, apellido, observa;

    public string tbusuario
    {
        get { return usuario; }
        set { usuario = value; }
    }

    public string tbclave
    {
        get { return clave; }
        set { clave = value; }
    }

    public string tbnombre
    {
        get { return nombre; }
        set { nombre = value; }
    }
    public string tbapellido
    {
        get { return apellido; }
        set { apellido = value; }
    }
    public string tbobserva
    {
        get { return observa; }
        set { observa = value; }
    }
}
}
